     public double sumar(){
      int contar=tabla.getRowCount();
       double sumar=0;
       for(int i=0;i<contar;i++){
           
       sumar=sumar+Double.parseDouble(tabla.getValueAt(i,3).toString());
       
       }
      lblmonto.setText(String.valueOf(sumar));

    
    
    return sumar;
    }
   
    
