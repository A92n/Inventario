     public Caja getdata(double sumar){
     Caja caj=new Caja();
     int contar=tabla.getRowCount();
      String nom;
      double total;
       for(int i=0;i<contar;i++){
           
    nom=(tabla.getValueAt(i,0).toString());
    total=Double.parseDouble(tabla.getValueAt(i,3).toString());
    caj.setProducto(nom);
       
       caj.setTotal(total);
       caj.setSuma(sumar);
       }
  
     return caj;
     }
     
    
     
  
    public void pago(double sumar){
        double recibido=Double.parseDouble(txtrecibido.getText());
        double cambio=0;
    if(recibido<sumar){
        
    JOptionPane.showMessageDialog(null, "Dinero insuficiente", "Pago insuficiente",JOptionPane.ERROR_MESSAGE);
    
    
    }
    else{
    cambio=recibido-sumar;
    }
    
    lblcambio.setText(String.valueOf(cambio));
    
    }
    
